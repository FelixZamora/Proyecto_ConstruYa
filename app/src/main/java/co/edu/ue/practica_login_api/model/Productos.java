package co.edu.ue.practica_login_api.model;

public class Productos {
    private String use_id;
    private String use_nombre;
    private String use_sku;
    private String use_precio;
    private String use_imagen;

    public String getUse_id() {
        return use_id;
    }

    public void setUse_id(String use_id) {
        this.use_id = use_id;
    }

    public String getUse_nombre() {
        return use_nombre;
    }

    public void setUse_nombre(String use_nombre) {
        this.use_nombre = use_nombre;
    }

    public String getUse_sku() {
        return use_sku;
    }

    public void setUse_sku(String use_sku) {
        this.use_sku = use_sku;
    }

    public String getUse_precio() {
        return use_precio;
    }

    public void setUse_precio(String use_precio) {
        this.use_precio = use_precio;
    }

    public String getUse_imagen() {
        return use_imagen;
    }

    public void setUse_imagen(String use_imagen) {
        this.use_imagen = use_imagen;
    }

}
